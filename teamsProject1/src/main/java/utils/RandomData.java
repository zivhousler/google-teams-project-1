    public static int generateRandomNumber(Integer min, int max) {
        if (min == null)
            return ThreadLocalRandom.current().nextInt(max);
        return ThreadLocalRandom.current().nextInt(min, max);
    }

    public static String generateRandomToken(){
        return String.valueOf(generateRandomNumber(100000000, 999999999));
    }

    public static String[] generateRandomShirts(int amount) {
        int rand;
        String[] shirts = new String[amount];
        for (int i = 0; i < amount; i++) {
            rand = generateRandomNumber(0, 100);
            if (Arrays.asList(shirts).contains(String.valueOf(rand))) {
                rand = generateRandomNumber(0, 100);
                i--;
                continue;
            }
            shirts[i] = Integer.toString(rand);
        }
        return shirts;
    }

